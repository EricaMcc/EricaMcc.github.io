////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if (num1 < num2) {
    return num1;
  } else {
    return num2;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num === 0) {
    return true;
  } else if (num === 1) {
    return false;
  } else {
    if (num > 0) {
      return isEven(num - 2);
    } else {
      return isEven(num + 2);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  var countChar = 0;
  for (var i = 0; i < string.length; i++) {
    if (string[i] === char) {
      countChar++;
    }
  }
  return countChar;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var countB = 0;
  for (var i = 0; i < string.length; i++) {
    if (string[i] === 'B') {
      countB++;
    }
  }
  return countB;
}

